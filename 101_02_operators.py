# arthematic operators
# +, - , *, /(will give float), **(power), //(will give integer), %

num1 = 10
num2 = 20
print('====== +')
print(num1+num2)

print('====== -')
print(num1-num2)

print('====== *')
print(num1*num2)

print('====== /')
print(num1/2)

print('====== **')
print(num1**2)

print('====== //')
print(num1//2)

